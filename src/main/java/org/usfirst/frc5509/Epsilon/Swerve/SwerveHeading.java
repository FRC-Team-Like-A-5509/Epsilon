package org.usfirst.frc5509.Epsilon.Swerve;

/**
 * Represents a full heading of where you want to drive the swerve module
 */
public class SwerveHeading {
    /**
     * Magnitude in the left/right direction
     */
    public double x;
    /**
     * Magnitude in the forward/backward direction
     */
    public double y;
    /**
     * Magnitude of rotation (positive is clockwise / "right")
     */
    public double rotation;

}
